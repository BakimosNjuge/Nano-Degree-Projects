package com.madonasyombua.myapplication.model;

public class VideoTrailer {
    String id;
    String key;
    String name;
    String site;
    String size;
    String type;


    public String getId() {
        return id;
    }

    public String getKey() {
        return key;
    }

    public String getName() {
        return name;
    }

    public String getSite() {
        return site;
    }

    public String getSize() {
        return size;
    }

    public String getType() {
        return type;
    }
}
